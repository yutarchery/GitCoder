int n;
ll c[105][105];

int main() {
  // cin の高速化
  std::cin.tie(nullptr);
  ios::sync_with_stdio(false);

  cin >> n;
  for (int i = 1; i <= n; i++) {
    for (int j = i + 1; j <= n; j++) {
      cin >> c[i][j];
    }
  }

  for (int i = 1; i <= n; i++) {
    for (int j = i + 1; j <= n; j++) {
      for (int k = j + 1; k <= n; k++) {
        if (c[i][k] > c[i][j] + c[j][k]) {
          Yes();
          return 0;
        }
      }
    }
  }

  No();

  return 0;
}
